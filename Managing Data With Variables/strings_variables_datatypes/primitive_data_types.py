# Data Types -  String | Integer | Float | Boolean

#String

print("Product Name: MacBook Air")
# Extracting each character called 'subscripting'
print("MacBook Air"[9])

print("MacBook Air Price with Tax: " + "800" + "12") # Concatnating two strings instead of doing math operation

# Integer - Numbers without any decimal places
print("MacBook Air Price with Tax: " + str(800 + 60) )

# NOTE: For Large Integers ( 123, 456, 789), you can underscore sign ( 123_456_789)

# Float (Floating Point Number) - Numbers with decimal places
print("MacBook Air Price with Tax: " + str(800.00 + 60.00) )

# Boolean
is_discounted = True


